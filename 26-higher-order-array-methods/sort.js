// names
const names = ["Johan", "Fredrik", "Saman", "Alicia", "Maja", "Elliot"];
console.log("Names in chaos:", names);

// sort array alphabetically
names.sort();

console.log("Names in proper order:", names);

// reverse order
names.reverse();
console.log("Names in reverse order:", names);
